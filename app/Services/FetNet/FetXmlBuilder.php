    private function emitIndependentGroups(): void
    {
        $this->w->startElement('Independent_Groups_List');
        $this->w->endElement();
    }

    // ─── Helpers ──────────────────────────────────────────────────────────

    private function uniqueLabel(string $base, array &$seen): string
    {
        $base = trim($base) ?: 'Unnamed';
        $candidate = $base;
        $i = 2;
        while (isset($seen[$candidate])) {
            $candidate = $base . '_' . $i++;
        }
        $seen[$candidate] = true;
        return $candidate;
    }

    private function collectStudentIds(Collection $rootStudents): array
    {
        $ids = [];
        $walk = function ($node) use (&$walk, &$ids) {
            $ids[] = $node->id;
            foreach ($node->children ?? [] as $c) $walk($c);
        };
        foreach ($rootStudents as $r) $walk($r);
        return $ids;
    }
